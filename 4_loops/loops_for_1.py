
# for loop

# range () - built-in function used to generate a sequence of numbers

# range(number)
for num in range(10):
    print(num)

# range(start:end) - end is exclusive
for num in range(1, 10):
    # print the number in range
    print(num)

# range(start:end:step)
for num in range(1,10,2):
    print(num)