hat = [1, 2, 3, 4, 5]

# slicing 
print(hat[0:2]) # select the first two items in hat list

print(hat[0:4]) # this will print the first 4 items in the list

# slicing shortcuts
print(hat[:4]) # print the first 4 items in the list

print(hat[2:4]) # print from the third item to the 4th item 


# use negative indexing
print(hat[-1:])  # this will print the last item in the list
print(hat[-2:]) # this will print the last two items in the list
print(hat[-3:]) # this will print the last three items in the list

print(hat[-5:-1:2])# what will this print?